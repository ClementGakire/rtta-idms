    public function index()
    {
        //
        //$users = User::all();
         $users = DB::table('users')
            ->leftJoin('roles', 'users.role_id', 'roles.id')
            ->select('users.*', 'roles.role')
            ->get();
           
        return view('users.index')->with('users', $users);
